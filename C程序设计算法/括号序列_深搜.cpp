#include <iostream>
#include <cstring>
#include <algorithm>
#include <set>
using namespace std;

int n;
string s;
set<string> ret;
//此时的左括号数 右括号数 添加的左括号数 右括号数 当前位置 当前序列   
void dfs(int l,int r,int al,int ar,int idx,string cur){
    if(idx==n){
        if(!al&&!ar)
            ret.insert(cur);
        return;
    }
    if(al)//要补的左括号数    ((()
        dfs(l+1,r,al-1,ar,idx,cur+'(');
    if(ar&&l>r) // 要补的右括号数 并且此时的左括号数大于右括号数才补右括号数  
        dfs(l,r+1,al,ar-1,idx,cur+')');
    if(s[idx]=='(')
        dfs(l+1,r,al,ar,idx+1,cur+'(');
    else if(l>r) // 补右括号数  
        dfs(l,r+1,al,ar,idx+1,cur+')');
}

int main(){
    cin>>s;
    n=s.size();
    int al=0,ar=0,l=0;
    for(int i=0;i<s.length();++i){
        if(s[i]=='('){  //((()  ()))
            l++;
        }
        else if(s[i]==')'){
            if(l==0)
                al++; //要补的左括号数  
            else{
                l--;
            }
        }
    }
    ar=l; 
    dfs(0,0,al,ar,0,"");
    cout<<ret.size()<<endl;
    
    for(set<string>::iterator it=ret.begin();it!=ret.end();it++){
		cout << *it << endl;
	}
    
    
}



